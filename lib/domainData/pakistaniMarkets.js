/**
 * Get all markets for a specific city
 * @param {string} city - City name (lowercase)
 * @returns {Array} Array of market objects with en and ur properties
 */
export function getMarketsForCity(city) {
  return pakistaniMarkets[city.toLowerCase()] || [];
}

/**
 * Get all cities
 * @returns {string[]} Array of city names
 */
export function getAllCities() {
  return Object.keys(pakistaniMarkets);
}

/**
 * Get all markets across all cities
 * @returns {Array} Array of all market objects
 */
export function getAllMarkets() {
  const allMarkets = [];
  Object.entries(pakistaniMarkets).forEach(([city, markets]) => {
    markets.forEach(market => {
      allMarkets.push({
        ...market,
        city: city.charAt(0).toUpperCase() + city.slice(1)
      });
    });
  });
  return allMarkets;
}

/**
 * Search markets by keyword (English or Urdu)
 * @param {string} keyword - Search term
 * @returns {Array} Matching market objects
 */
export function searchMarkets(keyword) {
  const searchTerm = keyword.toLowerCase();
  return getAllMarkets().filter(market =>
    market.en.toLowerCase().includes(searchTerm) ||
    market.ur.includes(keyword)
  );
}
